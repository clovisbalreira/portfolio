export class Conta {
    constructor(tipoConta, nome, porcentagem, valor, tempo, meta, valorMeta){
        this.tipoConta = tipoConta
        this.nome = nome;
        this.porcentagem = porcentagem;
        this.valor = valor;
        this.tempo = tempo;
        this.meta = meta;
        this.valorMeta = valorMeta;
    }
}